static inline bool
is_allowed_domain_ascii(unsigned char c)
{
    return (c >= 'A' && c <= 'Z')
           || (c >= 'a' && c <= 'z')
           || (c >= '0' && c <= '9')
           || c == '.' || c == '-' || c == '_' || c >= 0x80;
}

static inline bool
validate_domain(const char *domain)
{
    for (const char *ch = domain; *ch; ++ch)
    {
        if (!is_allowed_domain_ascii((unsigned char)*ch))
        {
            return false;
        }
    }

    return true;
}
